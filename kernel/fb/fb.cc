    static uint8_t fontScale = 1; // (size = scale * 8)
    limine_framebuffer_request framebuffer_request = {
        .id = LIMINE_FRAMEBUFFER_REQUEST,
        .revision = 0,
        .response = nullptr
    };
    limine_framebuffer *mainFramebuffer = nullptr;
    __attribute__((section(".bss"))) std::size_t screenX, screenY;

    void init(){
        if(framebuffer_request.response == (void*)0){
            dbg::print("NULL Framebuffer response");
            while(1){}
        }

        // TODO: Framebuffer picking
        mainFramebuffer = framebuffer_request.response->framebuffers[0];

        if(mainFramebuffer == nullptr){
            dbg::print("No framebuffer found\n");
            while (1){}
        }
        screenX = screenY = 0;
        setFontSize(8);
    }

    void printInfo(){
        std::printf("Framebuffer info:\n");
        std::printf("\tWidth:  %ld\n", mainFramebuffer->width);
        std::printf("\tHeight: %ld\n", mainFramebuffer->height);
        std::printf("\tbpp:    %d\n", mainFramebuffer->bpp);
    }

    void setFontSize(int size){
        if(size % 8 != 0){
            std::printf("Invalid font scaling, scaling must be a multiple of 8\n");
            std::printf("Remainder: %d\n", size%8);
            while(1){}
        }
        fontScale = size/8;
    }

    void setPixel(std::uint64_t x, std::uint64_t y, std::uint8_t r, std::uint8_t g, std::uint8_t b){
        if(x > mainFramebuffer->width || y > mainFramebuffer->height){
            dbg::print("X or Y pos oob");
            return;
        }

        std::uint64_t offset = (y*mainFramebuffer->width+x) * (mainFramebuffer->bpp/8);
        auto pixelAddr = reinterpret_cast<volatile std::uint8_t*>(mainFramebuffer->address)+offset;

        std::uint8_t rShift = mainFramebuffer->red_mask_shift;
        std::uint8_t gShift = mainFramebuffer->green_mask_shift;
        std::uint8_t bShift = mainFramebuffer->blue_mask_shift;

        std::uint32_t rMask = (1 << mainFramebuffer->red_mask_size) - 1;
        std::uint32_t gMask = (1 << mainFramebuffer->green_mask_size) - 1;
        std::uint32_t bMask = (1 << mainFramebuffer->blue_mask_size) - 1;

        *reinterpret_cast<volatile std::uint32_t*>(pixelAddr) &= ~(rMask << rShift);
        *reinterpret_cast<volatile std::uint32_t*>(pixelAddr) &= ~(gMask << gShift);
        *reinterpret_cast<volatile std::uint32_t*>(pixelAddr) &= ~(bMask << bShift);

        *reinterpret_cast<volatile std::uint32_t*>(pixelAddr) |= (r & rMask) << rShift;
        *reinterpret_cast<volatile std::uint32_t*>(pixelAddr) |= (g & gMask) << gShift;
        *reinterpret_cast<volatile std::uint32_t*>(pixelAddr) |= (b & bMask) << bShift;
    }

    static void getPixel(uint64_t x, uint64_t y, uint8_t *r, uint8_t *g, uint8_t *b){
        uint64_t fbwidth = mainFramebuffer->width;
        uint64_t fbheight = mainFramebuffer->height;
        if (x >= fbwidth || y >= fbheight){
            return;
        }

        uint64_t offset = (y * fbwidth + x) * (mainFramebuffer->bpp / 8);
        volatile uint8_t* pixelAddress = (volatile uint8_t*)(mainFramebuffer->address) + offset;
        
        uint32_t pixelValue = 0;
        for (uint64_t i = 0; i < mainFramebuffer->bpp / 8; ++i)
        {
            pixelValue |= pixelAddress[i] << (i * 8);
        }

        uint8_t rShift = mainFramebuffer->red_mask_shift;
        uint8_t gShift = mainFramebuffer->green_mask_shift;
        uint8_t bShift = mainFramebuffer->blue_mask_shift;
        uint32_t rMask = (1 << mainFramebuffer->red_mask_size) - 1;
        uint32_t gMask = (1 << mainFramebuffer->green_mask_size) - 1;
        uint32_t bMask = (1 << mainFramebuffer->blue_mask_size) - 1;

        *r = (pixelValue >> rShift) & rMask;
        *g = (pixelValue >> gShift) & gMask;
        *b = (pixelValue >> bShift) & bMask;
    }

    void fill(std::uint8_t r, std::uint8_t g, std::uint8_t b){
        for(std::uint64_t y = 0; y < mainFramebuffer->height; ++y){
            for(std::uint64_t x = 0; x < mainFramebuffer->width; ++x){
                setPixel(x, y, r, g, b);
            }
        }
    }

    static void scrollBack(int lines){
        for (uint64_t y = lines; y < mainFramebuffer->height; ++y){
            for (uint64_t x = 0; x < mainFramebuffer->width; ++x){
                uint8_t r, g, b;
                getPixel(x, y, &r, &g, &b);
                setPixel(x, y - lines, r, g, b);
            }
        }
        for (uint64_t y = mainFramebuffer->height - lines; y < mainFramebuffer->height; ++y){
            for (uint64_t x = 0; x < mainFramebuffer->width; ++ x){
                setPixel(x, y, 0, 0, 0);
            }
        }
    }

    void putchr(char c, uint8_t r, uint8_t g, uint8_t b){
        for(int y = 0; y < 8; ++y){
            for(int x = 0; x < 8; ++x){
                if(FONT[(uint8_t)c][y] & (1 << x)){
                    for(int sy = 0; sy < fontScale; ++sy){
                        for(int sx = 0; sx < fontScale; ++sx){
                            setPixel(screenX + x * fontScale + sx, screenY + y * fontScale + sy, r, g, b);
                        }
                    }
                }
            }
        }
        screenX+=(8*fontScale);
        if(screenX >= mainFramebuffer->width){
            screenX = 2;
            screenY+=(8*fontScale)+2;
        }
        if(screenY >= mainFramebuffer->height){
            scrollBack((8*fontScale)+2);
        }
    }

    void putc(char c){
        switch(c){
            case '\n': {
                screenY += (8*fontScale)+2;
                screenX = 2;
            } break;
            case '\r': {
                screenX = 2;
            } break;
            case '\t': {
                for(int i = 0; i < 4; ++i){
                    putc(' ');  
                }
            } break;
            default: {
                putchr(c, 255, 255, 255);
            } break;
        }
    }
